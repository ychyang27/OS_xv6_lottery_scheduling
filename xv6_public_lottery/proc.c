// Per-CPU process scheduler.
// Each CPU calls scheduler() after setting itself up.
// Scheduler never returns.  It loops, doing:
//  - choose a process to run
//  - swtch to start running that process
//  - eventually that process transfers control
//      via swtch back to the scheduler.
void
scheduler(void)
{
  struct proc *p;
  struct cpu *c = mycpu();
  // 스케쥴이 될 때 티켓의 수를 1로 맞추어준다. 즉 티켓의 default 값을 1로 설정해준다.
  // process가 실행될때 fork로 인해 parent로 부터 ticket을 1로 받기 때문에 최초 프로세서의 ticket을 1로 설정해주면 되겠다.
  ptable.proc[0].tix=1; 
  c->proc = 0;
 
  for(;;){
    // Enable interrupts on this processor.
    sti();
    // rand함수를 이용해 winner_ticket을 설정한다.
    const int winner_ticket = rand(totaltix())+1;
    // tix_count 는 전체 ptable에서 winner_ticket을 얻을 process 설정을 위한 변수
    int tix_count = 0;
    // Loop over process table looking for process to run.
    acquire(&ptable.lock);
    for(p = ptable.proc; p < &ptable.proc[NPROC]; p++){
      // tix_count에 ptable의 티켓 값을 더해준다.
      tix_count+=p->tix;
      // RUNNABLE 또는 SLEEPING 상태인 process들을 스케쥴 해줄 것이다.
      // => 사실 유저 테스트를 위한 부분. 실제 구현시에는 SLEEPING 상태의 process는 스케쥴하지 않아도 된다.
      if(p->state !=RUNNABLE && p->state != SLEEPING)
        continue;
      // 해당하는 winner_ticket에 따라 스케쥴할 프로세스를 결정한다.
      if(tix_count < winner_ticket){
        continue;
      }
/* .. 디버깅용. 설정한 뒤에 cprintf 횟수를 세어 보면 ticket의 개수에 비례하는 것을 통해 올바른 스케쥴이 됨을 확인할 수 있다.
      //if(strncmp(p->name,"init",4)!=0 && strncmp(p->name,"sh",2)!=0 && strncmp(p->name,"initcode",8)!=0 ){
        cprintf("timeline### %d  ",ticks);
        cprintf("<<%s>> RUN pid %d <- Winning ticket %d (total: %d)",p->name,p->pid,winner_ticket,totaltix());
        cprintf("(time : %d | tix : %d )\n",p->ticks,p->tix);
      //}
*/
      // Switch to chosen process.  It is the process's job
      // to release ptable.lock and then reacquire it
      // before jumping back to us.
      p->winner_tix = winner_ticket;
      c->proc = p;
      switchuvm(p);
      p->state = RUNNING;
      // ticks-start 값으로 실행한 시간을 측정할 것이다. (p->ticks)
      const int start = ticks;
      swtch(&(c->scheduler), p->context);
      p->ticks +=(ticks-start);
      p->count++;
      switchkvm();

      // Process is done running for now.
      // It should have changed its p->state before coming back.
      c->proc = 0;
      // 스케쥴이 되었으므로 break 해준다.
      break;
    }
    release(&ptable.lock);

  }
}

// Enter scheduler.  Must hold only ptable.lock
// and have changed proc->state. Saves and restores
// intena because intena is a property of this
// kernel thread, not this CPU. It should
// be proc->intena and proc->ncli, but that would
// break in the few places where a lock is held but
// there's no process.
void
sched(void)
{
  int intena;
  struct proc *p = myproc();

  if(!holding(&ptable.lock))
    panic("sched ptable.lock");
  if(mycpu()->ncli != 1)
    panic("sched locks");
  if(p->state == RUNNING)
    panic("sched running");
  if(readeflags()&FL_IF)
    panic("sched interruptible");
  intena = mycpu()->intena;
  swtch(&p->context, mycpu()->scheduler);
  mycpu()->intena = intena;
}

// Give up the CPU for one scheduling round.
void
yield(void)
{
  acquire(&ptable.lock);  //DOC: yieldlock
  myproc()->state = RUNNABLE;
  sched();
  release(&ptable.lock);
}

// A fork child's very first scheduling by scheduler()
// will swtch here.  "Return" to user space.
void
forkret(void)
{
  static int first = 1;
  // Still holding ptable.lock from scheduler.
  release(&ptable.lock);

  if (first) {
    // Some initialization functions must be run in the context
    // of a regular process (e.g., they call sleep), and thus cannot
    // be run from main().
    first = 0;
    iinit(ROOTDEV);
    initlog(ROOTDEV);
  }

  // Return to "caller", actually trapret (see allocproc).
}

// Atomically release lock and sleep on chan.
// Reacquires lock when awakened.
void
sleep(void *chan, struct spinlock *lk)
{
  struct proc *p = myproc();
  
  if(p == 0)
    panic("sleep");

  if(lk == 0)
    panic("sleep without lk");

  // Must acquire ptable.lock in order to
  // change p->state and then call sched.
  // Once we hold ptable.lock, we can be
  // guaranteed that we won't miss any wakeup
  // (wakeup runs with ptable.lock locked),
  // so it's okay to release lk.
  if(lk != &ptable.lock){  //DOC: sleeplock0
    acquire(&ptable.lock);  //DOC: sleeplock1
    release(lk);
  }
  // Go to sleep.
  p->chan = chan;
  p->state = SLEEPING;

  sched();

  // Tidy up.
  p->chan = 0;

  // Reacquire original lock.
  if(lk != &ptable.lock){  //DOC: sleeplock2
    release(&ptable.lock);
    acquire(lk);
  }
}

//PAGEBREAK!
// Wake up all processes sleeping on chan.
// The ptable lock must be held.
static void
wakeup1(void *chan)
{
  struct proc *p;

  for(p = ptable.proc; p < &ptable.proc[NPROC]; p++)
    if(p->state == SLEEPING && p->chan == chan)
      p->state = RUNNABLE;
}

// Wake up all processes sleeping on chan.
void
wakeup(void *chan)
{
  acquire(&ptable.lock);
  wakeup1(chan);
  release(&ptable.lock);
}

// Kill the process with the given pid.
// Process won't exit until it returns
// to user space (see trap in trap.c).
int
kill(int pid)
{
  struct proc *p;

  acquire(&ptable.lock);
  for(p = ptable.proc; p < &ptable.proc[NPROC]; p++){
    if(p->pid == pid){
      p->killed = 1;
      // Wake process from sleep if necessary.
      if(p->state == SLEEPING)
        p->state = RUNNABLE;
      release(&ptable.lock);
      return 0;
    }
  }
  release(&ptable.lock);
  return -1;
}

//PAGEBREAK: 36
// Print a process listing to console.  For debugging.
// Runs when user types ^P on console.
// No lock to avoid wedging a stuck machine further.
void
procdump(void)
{
  cprintf("\n\n");

  struct proc *p;
// UNUSED를 제외한 모든 프로세스들의 정보들을 출력한다.
  for(p = ptable.proc; p < &ptable.proc[NPROC]; p++){
    if(p->state == UNUSED)
      continue;
    cprintf("*** %d << %s >> pid%d [%d] <- WIN %d (tix %d of %d) || time : %d",ticks,
                    p->name,p->pid,p->state,p->winner_tix,p->tix,totaltix(),p->ticks);

    cprintf("\n");
  }
  cprintf("\n\n");
}
